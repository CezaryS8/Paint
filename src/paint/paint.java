    private void jPanel3MouseDragged(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jPanel3MouseDragged
        graphics2D = (Graphics2D)jPanel3.getGraphics();
        
        mouse_x2 = evt.getX();
        mouse_y2 = evt.getY();     
        w=mouse_x2-mouse_x1;
        h=mouse_y2-mouse_y1;
            
        graphics2D.setColor(brushColor);
        if(graphics2D!=null){
        graphics2D.setStroke(
        new BasicStroke(jComboBox2.getSelectedIndex()+1)
        );
        
        if(jComboBox1.getSelectedIndex()==0){
            graphics2D.drawLine(old_mouse_x, old_mouse_y, evt.getX(), evt.getY());
        }
        else if(jComboBox1.getSelectedIndex()==1){
            repaint();
            rysuj("drawRect");       
        }
        else if(jComboBox1.getSelectedIndex()==2){
            repaint();
            rysuj("fillRect");        
        }
        else if(jComboBox1.getSelectedIndex()==3){
            repaint();
            rysuj("drawOval"); 
        }
       
            old_mouse_x=evt.getX();
            old_mouse_y=evt.getY();
        }
    }//GEN-LAST:event_jPanel3MouseDragged

