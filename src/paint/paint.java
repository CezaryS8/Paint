    void rysuj(String funkcja){
        if(mouse_x2<mouse_x1){
                if(mouse_y2<mouse_y1){
                    if(funkcja=="drawRect")
                        graphics2D.drawRect(mouse_x2, mouse_y2, -1*w, -1*h);   
                    else if(funkcja=="fillRect")
                        graphics2D.fillRect(mouse_x2, mouse_y2, -1*w, -1*h); 
                    else if(funkcja=="drawOval")
                        graphics2D.drawOval(mouse_x2, mouse_y2, -1*w, -1*h);  
                }
                else{
                    if(funkcja=="drawRect")
                        graphics2D.drawRect(mouse_x2, mouse_y1, -1*w, h);  
                    else if(funkcja=="fillRect")
                        graphics2D.fillRect(mouse_x2, mouse_y1, -1*w, h);  
                    else if(funkcja=="drawOval")
                        graphics2D.drawOval(mouse_x2, mouse_y1, -1*w, h);  
                }
            }
            else if(mouse_y2<mouse_y1)   {
                if(funkcja=="drawRect")
                    graphics2D.drawRect(mouse_x1, mouse_y2, w, -1*h); 
                else if(funkcja=="fillRect")
                    graphics2D.fillRect(mouse_x1, mouse_y2, w, -1*h); 
                else if(funkcja=="drawOval")
                    graphics2D.drawOval(mouse_x1, mouse_y2, w, -1*h); 
            }
            else {
                if(funkcja=="drawRect")
                    graphics2D.drawRect(mouse_x1, mouse_y1, w, h);  
                if(funkcja=="fillRect")
                    graphics2D.fillRect(mouse_x1, mouse_y1, w, h);  
                if(funkcja=="drawOval")
                    graphics2D.drawOval(mouse_x1, mouse_y1, w, h); 
            }
    };
