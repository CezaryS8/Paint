    private void jPanel3MouseReleased(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jPanel3MouseReleased
         graphics2D.setStroke(new BasicStroke(jComboBox2.getSelectedIndex()+1));
         graphics2D.setColor(brushColor);
         
        if(jComboBox1.getSelectedIndex()==1){          
            rysuj("drawRect");  
        }
        else if(jComboBox1.getSelectedIndex()==2){          
            rysuj("fillRect");   
        }
        else if(jComboBox1.getSelectedIndex()==3){
            rysuj("drawOval");
        }
        else if(jComboBox1.getSelectedIndex()==4){
            if(temp==0){
                lx1=mouse_x1;
                ly1=mouse_y1;
                temp++;
            }
            else{
                graphics2D.drawLine(lx1, ly1, evt.getX(), evt.getY());
                temp--;
            }
        }
        else if(jComboBox1.getSelectedIndex()==5){    
            if(temp==0){
                startX=evt.getX();
                startY=evt.getY();
                lx1=evt.getX();
                ly1=evt.getY();
                temp++;
            }
            else if(sqrt((evt.getX()-startX)*(evt.getX()-startX)+(evt.getY()-startY)*(evt.getY()-startY))<10)
            {
                graphics2D.drawLine(lx1, ly1, startX, startY);
                temp--;
            }
            else{
                graphics2D.drawLine(lx1, ly1, evt.getX(), evt.getY());
                lx1=evt.getX();
                ly1=evt.getY();
            }
        }
    }//GEN-LAST:event_jPanel3MouseReleased

